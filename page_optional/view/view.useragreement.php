<p>Anda selanjutnya disebut "pengguna" dengan ini menyetujui kebijakan privasi dan persyaratan pengguna didalam komunitas kami.
  Privasi dan persyaratan ini dapat berubah sewaktu-waktu tanpa pemberitahuan.<br>
  Maka Anda dengan ini menyetujui kami untuk :
<br>
<ol>
  <li>Mengirimkan email kepada Anda mengenai promosi dan informasi produk dalam situs ini.</li>
  <li>Memberikan informasi seperti nomor telepon,alamat email serta alamat rumah Anda kepada pengguna lain didalam komunitas kami.</li>
  <li>Memberikan informasi pribadi untuk pengembangan aplikasi kami (termasuk data identitas, email, nomor telepon, dan lain-lain).</li>
  <li>Memberikan informasi kepada pihak ketiga seperti nama, email, nomor telepon, alamat Anda, dan lokasi Anda untuk tujuan transaksi.</li>
</ol>
<br>
Data pribadi yang kami kumpulkan dari Anda dapat kami gunakan, atau bagikan kepada atau dialihkan ke pihak ketiga
(termasuk perusahaan terkait, penyedia layanan pihak ketiga, penyedia layanan pihak ketiga dan penyedia layanan mereka dan perusahaan terkait,
 dan penjual atau pedagang pihak ketiga), untuk sebagian atau semua tujuan.
<br>
 Anda diwajibkan untuk :
 <ol>
   <li>Mengisi identitas diri secara baik dan benar.</li>
   <li>Memberikan informasi mengenai alamat dan lokasi secara akurat.</li>
   <li>Tidak memberikan informasi palsu atau tidak ada (termasuk nomor telepon, email, dan foto) baik pada informasi data diri dan transaksi.</li>
 </ol>
 <br>
 Kami memiliki hak untuk mengelola akun Anda, termasuk menghapus dan memblokir akun Anda apabila ada aktivitas yang mencurigakan yang
 bertentangan dengan perundang-undangan dan persyaratan pengguna dalam komunitas kami.
 <br>
 Serta mengelola aktivitas transaksi Anda dan memberikan informasi anda (termasuk transaksi, informasi identitas, email, dan nomor telepon) kepada pihak ketiga.
 <br>
 Untuk menjaga keamanan data pribadi anda dari pengungkapan, penyalinan, modifikasi, penghapusan yang tidak sah, atau resiko serupa.
  Maka kami menerapkan tindakan sebagai berikut :
 <ul>
   <li>Membatasi akses ke data pribadi untuk individu atau pengguna lain yang membutuhkan akses</li>
   <li>Menghapus atau melakukan pemblokiran pada akun Anda jika terjadi <i>Anonymisation</i> sesuai standar diamanatkan oleh hukum yang berlaku, ketika
   tidak lagi diperlukan untuk tujuan hukum atau bisnis.</li>
   <li>Menggunakan teknologi enkripsi didalam transaksi serta aktivitas didalam akun Anda</li>
 </ul>
 <br>
 Informasi untuk pihak ketiga antara lain memuat tautan ke situs web lain yang dioperasikan oleh pihak lain, seperti afiliasi bisnis, pedagang atau gerbang pembayaran
 (<i>Payment gateway</i>) kami. Kami bertanggung jawab atas praktik privasi situs web yang dioperasikan oleh pihak-pihak lain. Anda disarankan untuk memeriksa kebijakan privasi
 yang berlaku dari situs web tersebut untuk menentukan bagaimana mereka akan menangani setiap informasi yang mereka kumpulkan dari anda.
</p>
